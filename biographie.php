PNL innove d'ailleurs avec le clip Bené via une campagne de communication, qui lui permet de toucher son public via les téléphones. Le clip est suivi d'une annonce des concerts du groupe, à Paris (AccorHotels Arena), Lyon, Marseille, Lille, Nantes et Marseille en mai et juin 2017.
