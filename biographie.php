                          <p class="card-text">
                              Le groupe sort un premier EP intitulé Que la famille en mars 2015 et connaît, plus tard, 
                              le succès avec le morceau Le monde ou rien (dont une partie du clip est tourné à Scampia). 
                              La réussite de ce morceau donnera à leurs autres clips une nouvelle exposition, leur permettant à tous de dépasser le million de vues (la vidéo cumule en janvier 2018 plus de 86 millions de vues sur YouTube).

Le journal Les Inrocks décrit alors le groupe comme étant « l'attente numéro 1 dans le paysage du rap français ».

Peu après la sortie de leur premier album, le groupe annonce la sortie d'un second intitulé Le Monde Chico, qui sort finalement le 30 octobre 2015. 
Le premier extrait de ce projet est intitulé Plus Tony que Sosa. Une semaine avant la sortie de leur album, le groupe sort un nouvel extrait intitulé Oh Lala dont le clip, 
tourné en Islande, atteindra les trois millions de vues en une semaine sur la plateforme Youtube et cumule plus de 63 millions de vue en janvier 2018.

Lors de leur Planète Rap sur Skyrock du 16 au 20 novembre 2015, ils décorent chaque jour le studio d'une ambiance particulière (ex. Scarface, Dragon Ball)18, 
et envoient un singe pour les représenter, ce qui amuse leurs fans sur les réseaux sociaux. En effet, PNL maintient le mystère en n'accordant aucune interview.
