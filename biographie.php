                    <h2>Style musicale</h2>
                </div>
                <div class="row">
                    <div class="card" >
                        <div class="card-body">
                          <p class="card-text">
                              PNL se distingue par un style musical décrit comme « cru, hypnotique et émouvant qui souffle un vent frais sur le rap français » : c'est le cloud rap, pionniers du genre qu'ils ont popularisé dans l'Hexagone, et au-delà. 
                              Les deux rappeurs ont pour principaux thèmes l'argent, le trafic de stupéfiants, la famille et le sentiment de se différencier de la plupart des autres personnes. 
                              Cependant, loin des clichés habituels du rap, les deux rappeurs ne font pas l'apologie du crime ou du trafic, mais décrivent simplement le quotidien des quartiers avec un fatalisme et une certaine mélancolie. 
                              Ils font également très souvent référence aux dessins animés, au film Scarface ou aux jeux vidéo de leur enfance ainsi qu'aux mangas : 
                              Le Roi lion, Le Livre de la Jungle, The Legend of Zelda ou encore Dragon Ball Z et Great Teacher Onizuka avec leur titre Onizuka tout en évoquant la vie de cité.
