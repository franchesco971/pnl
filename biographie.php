                              Les deux frères évoluent d'abord en solo. Tarik sort sa première mixtape Le Son des Halls Vol. 1 en 2008. Le deuxième volume sort en 2011. 
                              Début 2013, il purge plusieurs mois d'incarcération à Fleury-Mérogis pour trafic de stupéfiants8.

De son côté, Nabil entame son projet 365 jours pour percer en novembre 2011, plus tard compilé en mixtape sous le nom de Calmement. Il utilise également le pseudonyme de Ladif.

Les productions des deux frères sont alors fondamentalement différentes. Mehdi Maizi, rédacteur pour l'Abcdr du Son, commente : « Ce qui m’a impressionné c’est leur métamorphose. 
C’est comme s'ils étaient passés à travers un laboratoire de rap et en étaient ressortis meilleurs ».
