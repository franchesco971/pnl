                      Les attentes qui reposaient sur les épaules du duo étaient nombreuses et il faut bien dire que dans l’ensemble, elles n’ont pas été déçues. 
                      Pendant plus d’une heure et demie de concert, les deux frères de PNL ont offert à leur public un spectacle unique, généreux et maîtrisé. 
                      De quoi faire mentir quelques-unes de leurs célèbres punchlines et en confirmer d’autres.

    "J’prends mon oseille, je chante, j’me casse de la te-boî"

Quand N.O.S, le plus jeune des deux frères, rappait cette phase au soleil sur le titre "Jsuis QLF" dévoilé sur YouTube il y a un an et demi, 
il n’imaginait peut-être pas encore jusqu’où le succès de Dans la légende, leur troisième projet, allait les mener. Alors qu’ils s’apprêtaient à sortir leur dernier album, 
les deux hommes qui mettent un point d’honneur à décliner les interviews, se refusaient aussi à l’exercice des concerts. Abonnés aux tournées des showcases et release party dans les boîtes d’Europe et d’Afrique du Nord, l’idée d’offrir à leurs fans une prestation à la Cigale, à L’Olympia ou dans quelque autre salle ne semblait même pas les effleurer. Seuls certains festivals, comme We Love Green en 2016, ont trouvé grâce à leurs yeux.

Sauf que depuis, le succès de l’album Dans la légende a changé la donne. 
Fort d’une certification de diamant obtenue en seulement huit mois, le groupe annonce en février les premières dates de sa tournée qui se conclura à Bercy le 29 juin 2017. 
Les fans se précipitent et la salle de 20 000 places se remplit en un temps record. Quelques mois plus tard, la date est reportée en novembre. 
La raison, selon le groupe qui s’est exprimé sur les réseaux sociaux : une volonté de donner le meilleur d’eux-mêmes, 
"d’aller au bout de (leurs) idées pour que ce spectacle soit à la hauteur de ce qu’'(ils veulent) faire". Fini donc l’époque où ils se cassaient de la "te-boî", on était prévenus.

    "L’public j’m’en fous, j’sais qu’il me lâchera"

Malgré ce qu’ils ont pu en dire dans le passé, le lien qui unit le groupe à son public est des plus solides. 
Autoproduits depuis le début et fonctionnant sans aucune promo média, les deux frères entretiennent avec leurs fans une relation directe et privilégiée qui se passe d’intermédiaires. 
Ce concert était l’occasion de le mesurer.

Dès leur arrivée sur scène à 21 heures 30, c’est la liesse dans la fosse et les gradins. 
Les deux jeunes hommes commencent par le morceau "Dans ta rue", extrait de leur deuxième projet Le Monde Chico. Sans introduction préalable, la communion opère immédiatement. 
C’est alors une heure et demie de "karaoké", de signes "Z" (Zoo) et de captations sur téléphone qui démarre. 
Les fans s’époumonent, connaissant absolument toutes les paroles du duo par cœur, si bien que par moments, sûrement touchés, les deux hommes passent le relais au public comme sur "Jusqu’au dernier gramme", morceau durant lequel N.O.S n’a quasiment pas touché à son couplet.

Tout au long du show, les deux frères, qui ne semblent pas prolixes d’habitude, font part à leur public de leur reconnaissance jusqu’à, parfois, en perdre leurs mots. 
Quand Ademo prend pour la première fois la parole, il bafouille un : "On est super heureux d’être là" et, entre plusieurs remerciements, 
N.O.S se permet à plusieurs reprises d’empoigner les téléphones de quelques heureux fans pour capter des vidéos à leur place avant de leur rendre via la sécurité.

    "Que la famille, que la famille, que la famille"

Porté par l’enthousiasme des fans et la maîtrise du duo, le concert s’est déroulé sans encombre du début jusqu’à la fin. 
Si Ademo et N.O.S ont jonglé avec équilibre entre les morceaux de leurs trois projets, aucun featuring n’a pour autant été interprété sur scène. 
Ce n’était pas faute d’avoir convoqué la mif puisque ceux qui constituent l’entourage proche du groupe étaient là pour partager ce moment avec eux.

En plus d’avoir invité les groupes MMZ, DTF ou encore F430, ersatz des deux frères qu’ils ont l’habitude de côtoyer en studio ou au quotidien, 
à se produire en première partie, l’entourage a été invité à monter sur scène pour le morceau "DA", interprété en fin de concert pour célébrer le chemin accompli. 
Famille, amis, artistes et membres du staff s’étaient réunis pour ce moment historique dans la carrière du groupe.

    "Nos clips méritent le Festival de Cannes"

Un élément important de la patte PNL réside dans le travail visuel qui entoure l’œuvre du groupe, 
totalement novateur dans leur approche de la musique. On se souvient notamment des trois courts-métrages réalisés pour illustrer les morceaux "Naha", "Onizuka", et "Bené", 
construisant au passage une vraie mythologie, connue sur le bout des doigts par les fans. 
Ce travail narratif et visuel est ici transposé en live via une scénographie léchée retraçant tous les éléments constitutifs de leur univers.

Ainsi, sur scène, on retrouvait l’arbre rappelant la savane du clip "La vie est belle" et un cœur géant faisant référence au premier projet du groupe nommé "Que la famille", 
mais aussi un travail graphique affiché sur les écrans géants, sur lesquels alternaient des visuels cosmiques, 
ou encore des images de jeux vidéo personnalisés à la manière d’un Mortal Kombat façon Tarterêts. Le point culminant a consisté en un rappel de leur trilogie, 
au moment du morceau "Naha", où les protagonistes ennemis présents dans la salle s’opposaient de chaque côté de la scène.

Bien sûr, tout n’était pas parfait et certains pourront reprocher au groupe le minimalisme de son jeu de scène. 
Cependant, et sur bien des points, il s’agissait d’un concert généreux et assez remarquable, pointilleux et unique comme le travail que PNL a pris l’habitude de livrer depuissess débuts en 2015.
